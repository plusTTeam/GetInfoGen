package InfoGen;

public class Par {
    private Integer val1;
    private Integer val2;

    public Par(Integer val1, Integer val2) {
        this.val1 = val1;
        this.val2 = val2;
    }
    
    public Integer getVal1() {
        return val1;
    }

    public void setVal1(Integer val1) {
        this.val1 = val1;
    }

    public Integer getVal2() {
        return val2;
    }

    public void setVal2(Integer val2) {
        this.val2 = val2;
    }
    
}
